// ############################## Timer 3 Interrupt routine ##############################
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
if(htim->Instance==TIM3){
timer++;											 // Adds 100uS every interrupt
if(timer==time){									 // Triggers TRIAC gate on desired time
HAL_GPIO_WritePin(GPIOA,GPIO_PIN_7,GPIO_PIN_SET);
timer=0;
HAL_TIM_Base_Stop_IT(&htim3);
}
}
}
//############################## External Interrupt routine ##############################
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
if(GPIO_Pin==GPIO_PIN_4){							// Zero cross detection on PA4
timer=0;
HAL_TIM_Base_Start_IT(&htim3);						// Starts timer on zero cross detection
HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET);
}
// ------------------ input pulses for pulse increment or decrement ------------------
if(GPIO_Pin==GPIO_PIN_0){
time-=1;
if(time<1)time=1;
}
if(GPIO_Pin==GPIO_PIN_1){
time+=1;
if(time>100)time=100;
}
if(GPIO_Pin==GPIO_PIN_2){
time=100;
}
}
